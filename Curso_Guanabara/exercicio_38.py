numero1_escolhido = int(input('Digite um número inteiro: '))
numero2_escolhido = int(input('Digite um segundo número inteiro: '))

if numero1_escolhido > numero2_escolhido:

    print (f'{numero1_escolhido} é maior do que o segundo número escolhido que foi {numero2_escolhido}')

elif numero1_escolhido < numero2_escolhido:

    print (f'{numero1_escolhido} é menor do que o segundo número escolhido que foi {numero2_escolhido})')

else: 
    print ('Os dois números são iguais!')    