entrada = input ('Você quer "Entrar" ou "Sair"? ')


if entrada == 'entrar': 
    print ('Você escolher entrar, parabéns guerreiro!')

elif entrada == 'sair':
    print ('Você quer sair, te vejo na próxima aventura guerreiro!')

else: 
    print ('Você não digitou nenhuma das respostas válidas, favor retornar!')

